<h2 id="articulo-iii-interdiccion-de-la-persecucion-penal-multiple" tabindex="-1"><a class="header-anchor" href="#articulo-iii-interdiccion-de-la-persecucion-penal-multiple" aria-hidden="true">#</a> Artículo III. Interdicción de la persecución penal múltiple.-</h2>
<p>Nadie podrá ser procesado, ni sancionado mas de una vez por un mismo hecho, siempre que se trate del mismo sujeto y fundamento. Este principio rige para las sanciones penales y administrativas. El derecho penal tiene preeminencia sobre el derecho administrativo.</p>
<p>La excepción a esta norma es la revisión por la Corte Suprema de la sentencia condenatoria expedida en alguno de los casos en que la acción está indicada taxativamente como procedente en este Código.</p>
