<h2 id="articulo-ii-presuncion-de-inocencia" tabindex="-1"><a class="header-anchor" href="#articulo-ii-presuncion-de-inocencia" aria-hidden="true">#</a> Artículo II. Presunción de inocencia.-</h2>
<ol>
<li>
<p>Toda persona imputada de la comisión de un hecho punible es considerada inocente, y debe ser tratada como tal, mientras no se demuestre lo contrario y se haya declarado su responsabilidad mediante sentencia firme debidamente motivada. Para estos efectos, se requiere de una suficiente actividad probatoria de cargo, obtenida y actuada con las debidas garantías procesales.</p>
<p>En caso de duda sobre la responsabilidad penal debe resolverse a favor del imputado.</p>
